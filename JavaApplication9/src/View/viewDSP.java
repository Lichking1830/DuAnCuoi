    private void fillDataSP(int index, List<DongSP> listDSP) {
        DongSP dsp = listDSP.get(index);
        txtAddMaDSP.setText(dsp.getMaDSP());
        txtAddTenDSP.setText(dsp.getTenDSP());
    }

    private void showDataTable(List<DongSP> list) {
        dtm.setRowCount(0);
        for (DongSP dsp : list) {
            dtm.addRow(dsp.showdata());
        }
    }

