    private void btAddDSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddDSPActionPerformed
        String ma = txtAddMaDSP.getText();
        String ten = txtAddTenDSP.getText();
        DongSP dsp = new DongSP(ma, ten);
        listDSP.add(dsp);
        JOptionPane.showMessageDialog(this, dspsi.add(dsp));
        listDSP = dspsi.getall();
    }//GEN-LAST:event_btAddDSPActionPerformed

